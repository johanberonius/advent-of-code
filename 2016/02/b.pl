#!/usr/bin/perl
use strict;


my ($x, $y) = (0, 2);
my %k = (
                          "2,0" =>  1,
            "1,1" =>  2,  "2,1" =>  3,  "3,1" => 4,
"0,2" => 5, "1,2" =>  6,  "2,2" =>  7,  "3,2" => 8, "4,2" => 9,
            "1,3" => 'A', "2,3" => 'B', "3,3" => 'C',
                          "2,4" => 'D',
);

while (<>) {
    chomp;
    my @d = split '';

    for my $d (@d) {
        $x-- if $d eq 'L' && $k{($x-1).",$y"};
        $x++ if $d eq 'R' && $k{($x+1).",$y"};
        $y-- if $d eq 'U' && $k{"$x,".($y-1)};
        $y++ if $d eq 'D' && $k{"$x,".($y+1)};
    }

    my $n = $k{"$x,$y"};
    print $n;
}

print "\n";
