#!/usr/bin/perl
use strict;
use POSIX qw(ceil);

my $n = 33_100_000;
my @n = (1, 2, 4, 6, 12, 24, 36, 48, 60, 120, 180, 240, 300, 360, 420, 840, 1680, 2520, 5040, 7560, 10080, 12600, 15120, 17640, 20160, 22680, 25200, 27720, 55440, 83160, 110880, 138600, 166320, 194040, 221760, 249480, 277200, 304920, 332640, 360360, 720720);

for (@n) {
    my $h = $_ + 785400;
    my $p = 0;
    for my $e (ceil($h/50)..$h) {
        $p += 11 * $e * !($h % $e);
    }

    print "House $h got $p presents.\n";

    # last if $i >= 10;
    last if $p >= $n;
}
